package com.example.zhoubiao.cxcourses.model;

public class CommonCourse {
	String courseName;
	String courseIntroduction;
	String courseIcon;
	/**
	 * @return the courseName
	 */
	public String getCourseName() {
		return courseName;
	}
	/**
	 * @param courseName the courseName to set
	 */
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	/**
	 * @return the courseIntroduction
	 */
	public String getCourseIntroduction() {
		return courseIntroduction;
	}
	/**
	 * @param courseIntroduction the courseIntroduction to set
	 */
	public void setCourseIntroduction(String courseIntroduction) {
		this.courseIntroduction = courseIntroduction;
	}
	/**
	 * @return the courseIcon
	 */
	public String getCourseIcon() {
		return courseIcon;
	}
	/**
	 * @param courseIcon the courseIcon to set
	 */
	public void setCourseIcon(String courseIcon) {
		this.courseIcon = courseIcon;
	}
	

}
